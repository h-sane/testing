def run_stage(stage_name, apps):
    print(f"\n{'='*60}")
    print(f"EXECUTING {stage_name.upper()}")
    print(f"Apps: {apps}")
    print(f"{'='*60}")
    
    stage_dir = os.path.join(EXPERIMENTS_DIR, stage_name)
    ensure_dir(stage_dir)
    
    results = {
        "stage": stage_name,
        "timestamp": datetime.now().isoformat(),
        "apps": apps,
        "steps": {}
    }
    
    # ---------------------------------------------------------
    # STEP 1: DRY RUN
    # ---------------------------------------------------------
    print(f"\n>>> STEP 1: DRY RUN")
    dry_run_dir = os.path.join(stage_dir, "dry_run")
    ensure_dir(dry_run_dir)
    
    try:
        run_all(
            apps=apps,
            output_dir=dry_run_dir,
            use_cache=True,
            use_vision=True,
            dry_run=True
        )
        results["steps"]["dry_run"] = "completed"
        collect_logs(stage_dir, dry_run_dir)
    except Exception as e:
        print(f"ERROR: Dry run failed: {e}")
        results["steps"]["dry_run"] = f"failed: {e}"

    # ---------------------------------------------------------
    # STEP 2: REAL RUN
    # ---------------------------------------------------------
    print(f"\n>>> STEP 2: REAL EXECUTION")
    real_run_dir = os.path.join(stage_dir, "real_run")
    ensure_dir(real_run_dir)
    
    try:
        run_all(
            apps=apps,
            output_dir=real_run_dir,
            use_cache=True,
            use_vision=True,
            dry_run=False
        )
        results["steps"]["real_run"] = "completed"
        collect_logs(stage_dir, real_run_dir)
        snapshot_cache(stage_dir)
        
        # Capture stats
        stats = parse_run_stats(real_run_dir)
        results["real_run_stats"] = stats
        save_json(os.path.join(stage_dir, "summary.json"), stats)
        
    except Exception as e:
        print(f"ERROR: Real run failed: {e}")
        results["steps"]["real_run"] = f"failed: {e}"

    # ---------------------------------------------------------
    # STEP 3: CACHE VALIDATION RUN
    # ---------------------------------------------------------
    print(f"\n>>> STEP 3: CACHE VALIDATION RUN")
    cache_run_dir = os.path.join(stage_dir, "cache_run")
    ensure_dir(cache_run_dir)
    
    try:
        run_all(
            apps=apps,
            output_dir=cache_run_dir,
            use_cache=True,
            use_vision=True, # Keep enabled to see if cache takes precedence
            dry_run=False
        )
        results["steps"]["cache_run"] = "completed"
        
        # Capture stats
        cache_stats = parse_run_stats(cache_run_dir)
        results["cache_run_stats"] = cache_stats
        
    except Exception as e:
        print(f"ERROR: Cache run failed: {e}")
        results["steps"]["cache_run"] = f"failed: {e}"

    print(f"\n{stage_name.upper()} COMPLETE")
    return results

