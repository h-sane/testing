def ensure_dir(path):
    if not os.path.exists(path):
        os.makedirs(path)

def save_json(path, data):
    with open(path, 'w') as f:
        json.dump(data, f, indent=2)

def snapshot_cache(stage_dir):
    """Copy current cache state to stage directory."""
    snapshot_dir = os.path.join(stage_dir, "cache_snapshot")
    if os.path.exists(snapshot_dir):
        shutil.rmtree(snapshot_dir)
    if os.path.exists(CACHE_DIR):
        shutil.copytree(CACHE_DIR, snapshot_dir)

def collect_logs(stage_dir, run_output_dir):
    """Collect specific debug logs from the run output."""
    logs = [
        "matcher_debug.log",
        "vlm_debug.log",
        "vision_error_debug.json"
    ]
    
    # Copy file-based logs if they exist in runs dir (some might be global)
    # The harness.logger creates run-specific logs, but debug logs are often global or in runs/
    # We will look in the specific run output directory
    
    for log in logs:
        src = os.path.join(run_output_dir, log)
        if os.path.exists(src):
            shutil.copy(src, stage_dir)
        else:
            # Check root runs dir as fallback for global debug logs
            src_global = os.path.join("testing", "runs", log)
            if os.path.exists(src_global):
                shutil.copy(src_global, stage_dir)

def parse_run_stats(run_dir):
    """Parse the summary.json generated by harness.main"""
    summary_path = os.path.join(run_dir, "summary.json")
    if os.path.exists(summary_path):
        with open(summary_path, 'r') as f:
            return json.load(f)
    return {}

